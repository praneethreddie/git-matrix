/**
 * Get the font matrix for a given character.
 * Returns the space matrix for unsupported characters.
 */
export function getCharMatrix(char) {
  const upper = char.toUpperCase();
  return FONTS[upper] || FONTS[' '];
}

/**
 * Get all available characters in the font.
 */
export function getAvailableChars() {
  return Object.keys(FONTS);
}

